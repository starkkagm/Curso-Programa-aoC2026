

namespace EstruturaRepeticao
{
    public class Calculos
    {
      public int Soma(int v1, int v2)
        {
            return v1 + v2;
        }
        public int Dividir(int v1, int v2)
        {
            return v1 / v2;
        }
        public int Subtrair(int v1, int v2)
        {
            return v1 - v2;
        }
        public int Multiplicar(int v1, int v2)
        {
            return v1 * v2;
        }



    }
}
